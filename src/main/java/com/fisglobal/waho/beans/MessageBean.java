package com.fisglobal.waho.beans;

public class MessageBean {

	
	private String msg;
	private String data;
	
	public MessageBean() {
		
	}
	public MessageBean(String msg) {
		
		this.msg = msg;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	

	
	
}
