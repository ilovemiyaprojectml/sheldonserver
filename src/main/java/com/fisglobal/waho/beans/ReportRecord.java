package com.fisglobal.waho.beans;

public class ReportRecord {
	private String last_name;
	private String first_name;
	private String user_eid;
	private String sched_time;
	private String sched_time_status;
	private String remarks;
	private String manager;
	
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getUser_eid() {
		return user_eid;
	}
	public void setUser_eid(String user_eid) {
		this.user_eid = user_eid;
	}
	public String getSched_time() {
		return sched_time;
	}
	public void setSched_time(String sched_time) {
		this.sched_time = sched_time;
	}
	public String getSched_time_status() {
		return sched_time_status;
	}
	public void setSched_time_status(String sched_time_status) {
		this.sched_time_status = sched_time_status;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}

	

}
